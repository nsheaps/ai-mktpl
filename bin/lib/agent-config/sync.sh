#!/usr/bin/env bash
#
# sync.sh - Sync subcommand for agent-config
#
# Syncs .ai content (rules, agents, commands) to Claude Code directories
# by creating directory symlinks.
#
# Usage:
#   agent-config sync [TYPE] [OPTIONS]
#
# Types:
#   _all      Sync all types (default if no type specified)
#   rules     Sync only rules
#   agents    Sync only agents
#   commands  Sync only commands
#
# Options:
#   -T, --target PATH  Sync to specified directory (default: .claude in repo root)
#   -u, --user         Sync to user ~/.claude directory
#   -d, --dry-run      Show what would be done without doing it (default)
#   -n, --no-dry-run   Actually perform the sync
#   -h, --help         Show help message
#
# Examples:
#   agent-config sync              # Dry-run sync all to .claude
#   agent-config sync -n           # Actually sync all to .claude
#   agent-config sync rules -n     # Sync only rules
#   agent-config sync --user -n    # Sync all to ~/.claude
#

set -euo pipefail

# Source common configuration and helpers
# shellcheck source=common.sh
source "$(dirname "${BASH_SOURCE[0]}")/common.sh"

# =============================================================================
# Sync Type Implementations
# =============================================================================

# sync_rules - Sync rules directory
sync_rules() {
    sync_type_directory "rules"
}

# sync_agents - Sync agents directory
sync_agents() {
    sync_type_directory "agents"
}

# sync_commands - Sync commands directory
sync_commands() {
    sync_type_directory "commands"
}

# =============================================================================
# Rules Documentation Generator
# =============================================================================

# _generate_rules_doc - Generate autogenerated documentation alongside rules symlink
#
# Creates a markdown file that documents the sync configuration and provides
# guidance on where to make changes.
#
_generate_rules_doc() {
    local doc_file="$TARGET_DIR/rules/$UPSTREAM_FOLDER.md"
    local timestamp=$(date -u +"%Y-%m-%d %H:%M:%S UTC")

    local content="<!-- this file is autogenerated. DO NOT EDIT! -->
<!-- last updated: $timestamp -->

WARNING: You have autoconfigured rules, agents, and commands. These paths are managed by automation. Do not edit them directly.

Strongly consider making any changes at the source location instead of within existing or new files managed outside of the symlink from the target location. Doing so helps ensure that your changes are preserved and correctly propagated, and re-usable by future sessions and others in the organization.

- **Source**: $ROOT_DIR
- **Target**: $TARGET_DIR

| Type     | Source                        | Symlinked From               |
|----------|-------------------------------|------------------------------|
| Rules    | \`<source>/$BASE_SYNC_PATH/rules/\`    | \`<target>/rules/$UPSTREAM_FOLDER\`    |
| Commands | \`<source>/$BASE_SYNC_PATH/commands/\` | \`<target>/commands/$UPSTREAM_FOLDER\` |
| Agents   | \`<source>/$BASE_SYNC_PATH/agents/\`   | \`<target>/agents/$UPSTREAM_FOLDER\`   |

Please make changes at the source location. Use the target location's agent rules to describe how to update them. Pay close attention to updating synced rules versus rules that describe working with the target repository.

Agent rules can be found at places like:

- \`**/AGENTS.md\`
- \`**/CLAUDE.md\`
- \`.claude/rules/**/*.md\`
- \`.github/copilot-instructions.md\`
- \`.github/instructions/*.instructions.md\`"

    if [[ "$DRY_RUN" == true ]]; then
        if [[ -f "$doc_file" ]]; then
            dryrun "Would update: $doc_file"
        else
            dryrun "Would create: $doc_file"
        fi
        dryrun "Contents:"
        while IFS= read -r line; do dryrun "  $line"; done <<< "$content"
    else
        local action="Created"
        [[ -f "$doc_file" ]] && action="Updated"
        mkdir -p "$(dirname "$doc_file")"
        echo "$content" > "$doc_file"
        success "$action: $doc_file"
    fi
}

# =============================================================================
# Help
# =============================================================================

_show_help() {
    cat <<EOF
Usage: agent-config sync [TYPE] [OPTIONS]

Sync .ai content to Claude Code directories by creating directory symlinks.

Types:
  _all      Sync all types (default)
  rules     Sync only rules (includes documentation file)
  agents    Sync only agents
  commands  Sync only commands

Options:
  -T, --target PATH  Sync to specified directory
                     (detected default: $ROOT_DIR/.claude)
  -u, --user         Sync to user ~/.claude directory
                     (detected: $HOME/.claude)
  -d, --dry-run      Show what would be done without doing it (default)
  -n, --no-dry-run   Actually perform the sync
  -h, --help         Show this help message

Examples:
  agent-config sync                    # Dry-run: show what would sync
  agent-config sync -n                 # Actually sync all types
  agent-config sync rules -n           # Sync only rules
  agent-config sync agents commands    # Sync agents and commands (dry-run)
  agent-config sync --user -n          # Sync to ~/.claude

Synced Content:
  .ai/rules/    -> <target>/rules/$UPSTREAM_FOLDER/
  .ai/agents/   -> <target>/agents/$UPSTREAM_FOLDER/
  .ai/commands/ -> <target>/commands/$UPSTREAM_FOLDER/
EOF
}

# =============================================================================
# Main Entry Point
# =============================================================================

# sync_main - Main entry point for sync subcommand
#
# Parses arguments and dispatches to appropriate sync functions.
#
# Usage: sync_main [TYPE...] [OPTIONS]
#
sync_main() {
    local types=()

    # Parse arguments
    while [[ $# -gt 0 ]]; do
        case $1 in
            _all|rules|agents|commands)
                types+=("$1")
                shift
                ;;
            -u|--user)
                TARGET_DIR="$HOME/.claude"
                shift
                ;;
            -T|--target)
                TARGET_DIR="${2:?--target requires a path}"
                shift 2
                ;;
            -d|--dry-run)
                DRY_RUN=true
                shift
                ;;
            -n|--no-dry-run)
                DRY_RUN=false
                shift
                ;;
            -h|--help)
                _show_help
                return 0
                ;;
            -*)
                error "Unknown option: $1"
                error "Run 'agent-config sync --help' for usage"
                return 1
                ;;
            *)
                error "Unknown type: $1"
                error "Valid types: _all, rules, agents, commands"
                return 1
                ;;
        esac
    done

    # Default to _all if no types specified
    [[ ${#types[@]} -eq 0 ]] && types=("_all")

    # Default target if not set
    [[ -z "$TARGET_DIR" ]] && TARGET_DIR="$ROOT_DIR/.claude"

    # Show header
    info "Source: $ROOT_DIR/$BASE_SYNC_PATH"
    info "Target: $TARGET_DIR"
    info "Upstream folder: $UPSTREAM_FOLDER"
    [[ "$DRY_RUN" == true ]] && dryrun "=== DRY RUN MODE ===" || true

    # Execute sync for each type
    local synced_rules=false
    for type in "${types[@]}"; do
        case $type in
            _all)
                sync_rules
                sync_agents
                sync_commands
                synced_rules=true
                ;;
            rules)
                sync_rules
                synced_rules=true
                ;;
            agents)
                sync_agents
                ;;
            commands)
                sync_commands
                ;;
        esac
    done

    # Generate rules documentation if rules were synced
    [[ "$synced_rules" == true ]] && _generate_rules_doc

    # Show completion message
    if [[ "$DRY_RUN" == true ]]; then
        info "Dry run complete. No changes were made."
        local cmd="agent-config sync"
        for type in "${types[@]}"; do
            [[ "$type" != "_all" ]] && cmd="$cmd $type"
        done
        [[ "$TARGET_DIR" == "$HOME/.claude" ]] && cmd="$cmd --user"
        [[ "$TARGET_DIR" != "$HOME/.claude" && "$TARGET_DIR" != "$ROOT_DIR/.claude" ]] && cmd="$cmd --target \"$TARGET_DIR\""
        info "To apply these changes, run:"
        info "  $cmd --no-dry-run"
    else
        success "Sync complete!"
    fi
}
